from enum import Enum, auto
from typing import Optional

class Move(Enum):
    ROCK = auto()
    PAPER = auto()
    SCISSORS = auto()


class Action(Enum):
    YOU_WON = auto()
    YOU_FAILED = auto()
    DRAW = auto()


def dispatch(your_choose: Move, opponent_choose: Move) -> Optional[Action]:
    """Returns:
        - True: move_one win
        - False: move_two win
        - None: draw
    """
    match (your_choose, opponent_choose):
        case (Move.ROCK, Move.ROCK) | (Move.PAPER, Move.PAPER) | (Move.SCISSORS, Move.SCISSORS):
            # it's draw
            return Action.DRAW
        case (Move.ROCK, Move.SCISSORS) | (Move.SCISSORS, Move.PAPER) | (Move.PAPER, Move.ROCK):
            # Move one is won
            return Action.YOU_WON
        case (Move.SCISSORS, Move.ROCK) | (Move.PAPER, Move.SCISSORS) | (Move.ROCK, Move.PAPER):
            # Move two is won
            return Action.YOU_FAILED


MOVES = {
    'A': Move.ROCK,
    'B': Move.PAPER,
    'C': Move.SCISSORS,
    'X': Move.ROCK,
    'Y': Move.PAPER,
    'Z': Move.SCISSORS
}

POINT_PER_MOVE = {
    Move.ROCK: 1,
    Move.PAPER: 2,
    Move.SCISSORS: 3,
}


POINTS_PER_ACTION = {
    Action.YOU_WON: 6,
    Action.YOU_FAILED: 0,
    Action.DRAW: 3,
}


